# A tuple is similar to a list, but immutable, you cannot change it after creation,defined using parentheses.
# tuples is faster than list
# value can not be changed
students = (
    "Anowar Hossain",
    "Kazi Shah Hamza",
    "Siddikur Rahman Masum",
)

print(students)
print(students[0])

# nested touples
studentsDetails = (
    ("Name : Anowar Hossain", "ID : 101", "Age : 23"),
    ("Name : Kazi Shah Hamza", "ID : 102", "Age : 25"),
    ("Name : Siddikur Rahman Masum", "ID : 103", "Age : 24"),
)
print(studentsDetails)
print('\n')
print(studentsDetails[0])
print('\n')
print(studentsDetails[1])
print('\n')
